
public class TotemHeads {

    public static void main(String[] args) {
        /************************************************************************************************************
         * @file    TotemHeads.java
         * @brief   This program implement the idea of System.out.print to print three different faces on the screen
         * @author  Frank Liu,Tongtong
         * @date    September 10, 2019
         ************************************************************************************************************/

        System.out.println("    |||||||||||||||| ");    // Use the code System.out.println to print components of the face.
        System.out.println("    |              | ");
        System.out.println("    |              | ");
        System.out.println("    |   o      o   | ");
        System.out.println("   _|              |_");
        System.out.println("  |_                _|");
        System.out.println("    |  |________|  |");
        System.out.println("    |              |");
        System.out.println("    |              |");
        System.out.println("    ||||||||||||||||");
        System.out.println("    |  __      __  |");
        System.out.println("    | !  ! __ !  ! |");
        System.out.println("    | !o !/  \\!o ! |");     //be sure that in order to show "\", java needs to "\\"
        System.out.println("    | !__!    !__! |");
        System.out.println("    |              |");
        System.out.println("    |    ///|\\\\\\   |");
        System.out.println("     \\            /");
        System.out.println("      \\     o    /");
        System.out.println("       \\ _______/");
        System.out.println("    ||||||||||||||||");
        System.out.println("   /                \\");
        System.out.println("  /   _____   _____  \\");
        System.out.println(" |   |_(0)_| |_(0)_|  |");
        System.out.println("o|                    |o");   // create a new third head on my own
        System.out.println(";|          6         |;");
        System.out.println("  \\                  /");
        System.out.println("   \\      o__o      /");
        System.out.println("    \\______________/ ");

    }
}